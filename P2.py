# 연산자
a = 10 
b = 10 - 5 # 정수 빼기
c = 10 + 5
d = 10 * 5 
e = 10 / 5 # /는 파이썬에서는 실수나누기
# 자바에서는 정수 나누기이다
f = 10 / 3 # 소수점 자리는 16번째까지 표기? 17에서 반올림
g = 5 // 2 # 정수나누기, 몫만 나옴
h = 5 % 2 # 나머지 값 구하기
#경우의수, 3으로나누면 0,1,2 로 3개, 2로 나누면 0,1 로 2개
# 이러한 경우를 통해서 알고리즘 문제를 해결
print("b =", b)
print("c =", c)
print("d =", d)
print("e =", e)
print("f =", f)
print("g =", g)
print("h =", h)

qwer = a - b
print("qwer =", qwer)